void SystemClock_Config(void);

int main(void)
{
  LL_APB1_GRP2_EnableClock(LL_APB1_GRP2_PERIPH_SYSCFG);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);
  RCC->APB2ENR |= RCC_APB2ENR_SYSCFGEN;

  SystemClock_Config();

  MX_GPIO_Init();

  init_DS3231();
  init_IN12();
  init_BUTTONS();
  init_Control();
  
  while (1)
  {
    clockControl();
  }


}


